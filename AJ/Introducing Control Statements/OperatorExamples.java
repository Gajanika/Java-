public class OperatorExamples {
    public static void main(String[] args) {
        // Increment operator
        int a = 5;
        a++; // Equivalent to a = a + 1
        System.out.println("Increment: " + a); // Output: 6

        // Decrement operator
        int b = 10;
        b--; // Equivalent to b = b - 1
        System.out.println("Decrement: " + b); // Output: 9

        // Add and assign operator
        int c = 7;
        c += 2; // Equivalent to c = c + 2
        System.out.println("Add and assign: " + c); // Output: 9

        // Subtract and assign operator
        int d = 15;
        d -= 2; // Equivalent to d = d - 2
        System.out.println("Subtract and assign: " + d); // Output: 13

        // Multiply and assign operator
        int e = 4;
        e *= 3; // Equivalent to e = e * 3
        System.out.println("Multiply and assign: " + e); // Output: 12

        // Divide and assign operator
        int f = 20;
        f /= 4; // Equivalent to f = f / 4
        System.out.println("Divide and assign: " + f); // Output: 5

        // Modulus and assign operator
        int g = 17;
        g %= 5; // Equivalent to g = g % 5
        System.out.println("Modulus and assign: " + g); // Output: 2
    }
}

